  MOV    AH, 09H
  MOV    AL, [SI]
  INT    21H

  ADD    SI, 1

  MOV    AH, 09H
  MOV    AL, SI
  INT    21H

  MOV AH, 4CH
  INT 21H

CODESG ENDS
